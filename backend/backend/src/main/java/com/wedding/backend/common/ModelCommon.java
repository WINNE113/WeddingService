package com.wedding.backend.common;

/**
 * The {@code ModelCommon} class contains constant values representing roles in the system.
 * These roles are used for access control and user management.
 */
public class ModelCommon {
    /**
     * Represents the role for administrators.
     */
    public static final String ADMIN = "ROLE_ADMIN";
    /**
     * Represents the role for management personnel.
     */
    public static final String MANAGE = "ROLE_MANAGE";
    /**
     * Represents the role for regular users.
     */
    public static final String CUSTOMER = "ROLE_CUSTOMER";
}
